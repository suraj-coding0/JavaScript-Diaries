//for Loop 

for(let i = 1; i<=5; i++){            //five time print my name
    console.log("suraj")
}
    
for(let i = 1; i<=5; i++){            //counting print
    console.log(i)
}

for(let i = 5; i>0; i--){            //reverse counting print
    console.log(i)
}


// iterartion:- how many times loop executed

for(let i = 1; i<=10; i++){
    if(i==5)
        break;                //break keyword
    else
        console.log(i)
}

for(let i = 1; i<=6; i++){
    if(i==3)
        continue;            //continue keyword
    else
        console.log(i)
}